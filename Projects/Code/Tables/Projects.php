    protected $title;

    /**
     * @var integer
     *
     * @ORM\Column(name="category_id", type="integer", length=11, nullable=false)
     */
    protected $category_id;

    /**
     * @var integer
     *
     * @ORM\Column(name="customer_id", type="integer", length=11, nullable=false)
